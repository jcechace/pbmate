// validateUsersAndRoles checks that UsersAndRoles is only set when
// namespaces are non-empty and all entries are whole-database patterns
// (ending with ".*"). This mirrors PBM's ValidateUsersAndRolesOpt.
func validateUsersAndRoles(op string, usersAndRoles bool, namespaces []string) error {
	if !usersAndRoles {
		return nil
	}
	if len(namespaces) == 0 {
		return fmt.Errorf("%s: users-and-roles is only valid for selective operations (namespaces must be set)", op)
	}
	for _, ns := range namespaces {
		if !strings.HasSuffix(ns, ".*") {
			return fmt.Errorf("%s: users-and-roles requires whole-database namespace patterns (\"db.*\"), got %q", op, ns)
		}
	}
	return nil
}

// validateNamespaceRemap checks that NamespaceFrom and NamespaceTo are
// either both set or both empty.
func validateNamespaceRemap(op, from, to string) error {
	if (from != "") != (to != "") {
		return fmt.Errorf("%s: namespace-from and namespace-to must be set together", op)
	}
	return nil
}
